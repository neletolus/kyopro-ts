import * as fs from "fs";

const main = (input: string) => {
    const N = BigInt(input.trim());

    // 戦略:
    // 残り a 個のとき 1〜⌊a/2⌋ 個取れる。取れなくなった方が負け。
    //
    // 小さい a で勝敗 (lose = 手番が負け) を後退帰納法で確認:
    //   a=1  : 取れる範囲 [1, 0] → 取れない → lose
    //   a=2  : take 1 → 残 1 (lose) → win
    //   a=3  : take 1 → 残 2 (win) しか選べない → lose
    //   a=4  : take 1 → 残 3 (lose) → win
    //   a=5,6: 残せる範囲が 3 を含む → win
    //   a=7  : 残せる範囲 [4,6]、どれも win → lose
    //   ...
    //   a=15 : 残せる範囲 [8,14]、どれも win → lose
    //
    // 一般化:
    //   負け位置は a = 2^k - 1 (= 1, 3, 7, 15, 31, ...)
    //
    // 証明スケッチ:
    //   - a が 2^k - 1 形なら、取れる範囲は [1, 2^(k-1)-1]、残せる範囲は
    //     [2^(k-1), 2^k - 2]。この区間に 2^j - 1 形は存在しないので相手に
    //     負け位置を渡せず、相手は (帰納法で) 勝ち位置 → 自分は負け。
    //   - a が 2^k - 1 形でなければ、a ∈ [2^k, 2^(k+1) - 2] となる k が取れて、
    //     残り 2^k - 1 にする手が常に範囲内 (1 ≤ a - (2^k-1) ≤ ⌊a/2⌋)。
    //     よって相手を負け位置に追い込める → 自分は勝ち。
    //
    // 判定:
    //   N + 1 が 2 のべき乗 ⇔ N = 2^k - 1。
    //   2 のべき乗かは「ビット 1 が 1 個だけ」⇔ ((N+1) & N) === 0。
    //
    // 数値型について:
    //   N ≤ 10^18 は number の safe integer (≈ 9*10^15) を超えるので BigInt 必須。

    const M = N + 1n;
    if ((M & N) === 0n) {
        console.log("Second");
    } else {
        console.log("First");
    }
};

main(fs.readFileSync("/dev/stdin", "utf8").trimEnd());
